  componentWillMount() {
    /* This is before the component gets rendered for the first time
       add some event listeners for keyboard presses
    */
    document.addEventListener('keydown', (e) => {
      switch (e.keyCode) {
        case 65:// a
        case 37:// left arrow
          // move left
          this.movePlayerPosition(0, -1);
          break;
        case 68:// d
        case 39:// right arrow
          // move right
          this.movePlayerPosition(0, 1);
          break;
        case 87:// w
        case 38:// up arrow
          this.movePlayerPosition(-1, 0);
          // move up
          break;
        case 83:// s
        case 40:// down arrow
          // move down
          this.movePlayerPosition(1, 0);
          break;
        default:
      }
    });
  }

  movePlayerPosition(relRow, relCol) {
    const moveToRow = this.state.player.row + relRow;
    const moveToCol = this.state.player.col + relCol;

    // Don't update the position if player will be moving onto a blocking square
    if (this.state.map[moveToRow][moveToCol] === 1) {
      return;
    }

    this.setState({
      player: {
        row: moveToRow,
        col: moveToCol,
      },
    });
  }

