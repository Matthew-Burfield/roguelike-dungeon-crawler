const Map = ({ map, player }) =>
  <div className="gameMap">
    {map.map((row, index) => {
      if (player.row === index) {
        return <Row row={row} player={player} />;
      }
      return <Row row={row} />;
    })}
  </div>;

Map.propTypes = {
  map: React.PropTypes.arrayOf(React.PropTypes.array).isRequired,
  player: React.PropTypes.shape({
    row: React.PropTypes.number.isRequired,
    col: React.PropTypes.number.isRequired,
  }).isRequired,
};

const Row = ({ row, player }) =>
  <div className="gameRow">
    {row.map((col, index) => {
      if (player && player.col === index) {
        return <Square col={col} player={player} />;
      }
      return <Square col={col} />;
    })}
  </div>;

Row.propTypes = {
  row: React.PropTypes.arrayOf(React.PropTypes.number).isRequired,
  player: React.PropTypes.shape({
    row: React.PropTypes.number.isRequired,
    col: React.PropTypes.number.isRequired,
  }),
};

const Square = ({ col, player }) => {
  const className = col === 1 ? 'block' : 'walkable';
  let color = className === 'block' ? '#212121' : '#BDBDBD';
  if (player) color = '#FF5722';

  const style = {
    width: '10px',
    height: '10px',
    display: 'inline-block',
    color,
  };

  return (
    <div className={className} style={style}>
      {player && 'X'}
      {!player && col}
    </div>
  );
};

Square.propTypes = {
  col: React.PropTypes.number.isRequired,
  player: React.PropTypes.shape({
    row: React.PropTypes.number.isRequired,
    col: React.PropTypes.number.isRequired,
  }),
};

export default App;
